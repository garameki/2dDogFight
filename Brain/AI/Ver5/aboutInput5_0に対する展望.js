


過去分をためて入力することの効果について
ある時刻での入力aは一つ。その一つの入力に対する結果をAとする。
ある一定時間幅の入力を３つとしよう。その中にaも含まれるとしよう。そして、３つの結果をBとする。
すると、AがB同じ場合も、違う場合もあり得る。
つまり、aはBに使われているにもかかわらず、AとBが逆の結果になることもある。
なので、過去分をためて入力するということは、「トータルに考えると」、という入力方法だと言える。



